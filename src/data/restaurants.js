// Helper: check if a restaurant is open now
export function isOpenNow(restaurant) {
  const now = new Date();
  const hour = now.getHours() + now.getMinutes() / 60;
  if (restaurant.closeHour > 24) {
    // Wraps past midnight (e.g. closes at 2 AM = 26)
    return hour >= restaurant.openHour || hour < restaurant.closeHour - 24;
  }
  return hour >= restaurant.openHour && hour < restaurant.closeHour;
}

